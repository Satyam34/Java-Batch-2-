/* object-oriented programming system is a programming concept that is based on “objects”.
Objects are like real-world things in your program that have information and can do actions.
OOP uses key ideas like:
Encapsulation: Keeping data and actions together in one place.
Inheritance: Creating new objects based on existing ones.
Polymorphism: Using the same action in different ways for different objects.
Abstraction: Hiding complex details and showing only the essential features
OOP deals with objects and how they interact, whereas procedural programming is more interested with functions and linear processes.
*/